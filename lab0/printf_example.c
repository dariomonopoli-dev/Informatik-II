#include <stdio.h>
int main()
{
   char ch = 'A';
   char str[20] = "hello world";
   float flt = 10.234;
   int no = 150;
   double dbl = 12.81239;
   printf("Character is %c \n", ch);
   printf("String is %s \n" , str);
   printf("Float value is %f \n", flt);
   printf("Integer value is %d\n" , no);
   printf("Double value is %lf \n", dbl);
   return 0;
}
